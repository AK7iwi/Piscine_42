#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	*a;

	a = str;
	while (*a)
	{
		if (*a >= 'A' && *a <= 'Z')
		{
			*a += 32;
		}
		a++;
	}
	return (str);
}
