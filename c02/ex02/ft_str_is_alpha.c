#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		if (!(str[a] >= 'a' && str[a] <= 'z'))
		{
			if (!(str[a] >= 'A' && str[a] <= 'Z'))
				return (0);
		}
		a++;
	}
	return (1);
}
